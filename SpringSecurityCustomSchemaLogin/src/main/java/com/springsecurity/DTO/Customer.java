package com.springsecurity.DTO;

public class Customer {
	private String xusername;
	private String xpassword;
	private String xemail;
	private String xrole;
	private String xenable;
	private String xaddress;
	public String getXusername() {
		return xusername;
	}
	public void setXusername(String xusername) {
		this.xusername = xusername;
	}
	public String getXpassword() {
		return xpassword;
	}
	public void setXpassword(String xpassword) {
		this.xpassword = xpassword;
	}
	public String getXemail() {
		return xemail;
	}
	public void setXemail(String xemail) {
		this.xemail = xemail;
	}
	public String getXrole() {
		return xrole;
	}
	public void setXrole(String xrole) {
		this.xrole = xrole;
	}
	public String getXenable() {
		return xenable;
	}
	public void setXenable(String xenable) {
		this.xenable = xenable;
	}
	public String getXaddress() {
		return xaddress;
	}
	public void setXaddress(String xaddress) {
		this.xaddress = xaddress;
	}
	
}
